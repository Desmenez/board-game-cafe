function uniqWords(text: string): string[] {
  const parts = text.split(/\s+/).filter((w) => w.length > 0);
  const seen = new Set<string>();
  const out: string[] = [];
  for (const w of parts) {
    if (!seen.has(w)) {
      seen.add(w);
      out.push(w);
    }
  }
  return out;
}

